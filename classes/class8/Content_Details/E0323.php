            <h3>Solving Equations</h3>
            <p><strong><u>Solving Equations having linear expression/equation on one side and number on the other</u></strong></p>
            <p>There are various methods to solve the algebraic equations:</p>
            <p><strong>1. By addition/subtraction and multiplication/division</strong></p>
            <p>Example, an algebraic equation is 2x - 3 = 7</p>
            <p>Adding 3 both sides, 2x - 3 + 3 = 7 + 3 or 2x = 10</p>
            <p>Dividing both sides by 2, 2x/2 = 10/2 or <strong>x = 5</strong></p>
            <p>&nbsp;</p>
            <p><strong>2. By transposing</strong></p>
            <p>&nbsp;Example, an algebraic equation is x - 3 = 7</p>
            <p>Transposing 3 to RHS, x = 7 + 3 or <strong>x = 10</strong></p>
            <p>&nbsp;</p>
            <p><strong>3. Using combination of above 2 methods</strong></p>
            <p>Example, an algebraic equation is x/3 + 5/2 = -3/2</p>
            <p>Transposing 5/2 to RHS, x/3 = -3/2- 5/2 or x/3= -8/2 or x/3 = -4</p>
            <p>Multiplying both sides by 3, x/3 * 3 = -4 * 3 or <strong>x = -12</strong></p>
