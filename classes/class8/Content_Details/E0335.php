            <p>&nbsp;<strong>Problem</strong>: Find the angle measures x in the following figures:</p>
            <p><img src="img/803anglesum3.jpg" alt="" width="155" height="135" /></p>
            <p>Using angle sum property,</p>
            <p>50&deg; + 130&deg; + 120&deg; + x = 360&deg;</p>
            <p>300&deg; + x = 360&deg;</p>
            <p>x = 60&deg;</p>
            <p>&nbsp;</p>
            <p><img src="img/803anglesum4.jpg" alt="" width="182" height="122" /></p>
            <p>Using angle sum property,</p>
            <p>90&deg; + 60&deg; + 70&deg; + x = 360&deg;</p>
            <p>220&deg; + x = 360&deg;</p>
            <p>x = 140&deg;</p>
            <p><img src="img/803anglesum5.jpg" alt="" width="152" height="132" /></p>
            <p>First base interior angle = 180&deg; - 70&deg; = 110&deg;</p>
            <p>Second base interior angle = 180&deg; - 60&deg; = 120&deg;</p>
            <p>For 5 sides, angle sum of polygon = (n-2)*180&deg; = 540&deg;,</p>
            <p>So, 30&deg; + x + 110&deg; + 120&deg; + x = 540&deg;</p>
            <p>260&deg; + 2x = 540&deg;</p>
            <p>x = 140&deg;</p>
            <p><img src="img/803anglesum6.jpg" alt="" width="140" height="112" /></p>
            <p>Using angle sum property of polygon = (n-2)*180&deg;</p>
            <p>= (5-2)*180&deg;</p>
            <p>= 540&deg;</p>
            <p>So, x + x + x+ x+ x = 540&deg;</p>
            <p>5x= 540&deg;</p>
            <p>x = 108&deg;</p>
