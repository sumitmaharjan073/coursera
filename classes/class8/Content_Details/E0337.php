            <p><strong>Problem</strong>: The measures of two adjacent angles of a parallelogram are in the ratio 3:2. Find the measure of each of the angles of the parallelogram.</p>
            <p><strong>Solution</strong>:</p>
            <p><img src="img/803kindofquad6.jpg" alt="" width="247" height="162" /></p>
            <p>Let two adjacent angles be 3x and 2x.</p>
            <p>Since the adjacent angles in a parallelogram are supplementary,</p>
            <p>3x + 2x = 180&deg;</p>
            <p>5x = 180&deg;</p>
            <p>x = 36&deg;</p>
            <p>Therefore, first angle = 3x = 3 * 36&deg; = 108&deg;</p>
            <p>Second Angle = 2x = 2 * 36&deg; = 72&deg;</p>
            <p>&nbsp;</p>
            <p><strong>Problem</strong>: Two adjacent angles of a parallelogram have equal measure. Find the measure of the angles of the parallelogram.</p>
            <p><strong>Solution</strong>: Let each adjacent angle be x.</p>
            <p>Since the adjacent angles in a parallelogram are supplementary,</p>
            <p>x + x = 180&deg;</p>
            <p>x = 90&deg;</p>
            <p>Hence, each adjacent angle is 90&deg;</p>
            <p>Now, the other angles would be also 90&deg;.</p>
