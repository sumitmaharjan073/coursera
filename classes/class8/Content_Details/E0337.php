            <h3>Kinds of Quadrilaterals</h3>
            <p><strong><u>Kinds of Quadrilaterals</u></strong></p>
            <p>Quadrilaterals are classified based on the nature of their sides and angles.</p>
            <ul>
            <li><strong>Trapezium</strong> - It has a pair of sides which are parallel.</li>
            </ul>
            <p><img src="img/803kindofquad1.jpg" alt="" width="351" height="142" /></p>
            <ul>
            <li><strong>Kite</strong> - It is a diamond shaped quadrilateral with two distinct consecutive pairs of sides of equal length.</li>
            </ul>
            <p><img src="img/803kindofquad2.jpg" alt="" width="285" height="377" /></p>
            <ul>
            <li><strong>Parallelogram</strong> - A parallelogram has two distinct consecutive pairs of parallel sides of equal length.
            <ol>
            <li>The opposite sides of a parallelogram are equal.</li>
            <li>The opposite angles of a parallelogram are equal.</li>
            <li>The adjacent angles in a parallelogram are supplementary.</li>
            <li>Diagonals of a parallelogram bisect each other.</li>
            </ol>
            </li>
            </ul>
            <p><img src="img/803kindofquad3.jpg" alt="" width="322" height="277" /></p>
            <p><img src="img/803kindofquad4.jpg" alt="" width="236" height="110" /></p>
            <p>AB, CD and BC, AD are pairs of <strong>opposite sides</strong>.</p>
            <p>&ang;A, &ang;C and &ang;B, &ang;D are pairs of <strong>opposite angles.</strong></p>
            <p>AB-BC, BC-CD, CD-DA and DA-AB are <strong>adjacent sides</strong>.</p>
            <p>&ang;A-&ang;B,&ang;B-&ang;C, &ang;C-&ang;D and &ang;D-&ang;A are <strong>adjacentangles.</strong></p>
