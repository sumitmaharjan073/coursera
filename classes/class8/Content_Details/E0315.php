    <p><strong><u>Additive and Multiplicative identity of Rational numbers</u></strong></p>
    <ul>
    <li><strong>Additive identity: Zero</strong> is the additive identity for Rational, natural, whole numbers and integers, since adding it to them does not change the result.
    <ul>
    <li>3 + 0 = 3</li>
    <li>-4/5 + 0 = -4/5</li>
    </ul>
    </li>
    </ul>
    <p>Hence, 0 + a = a + 0 = a, where a can be rational number or natural number or whole number of integer.</p>
    <ul>
    <li><strong>Multiplicative identity: One</strong> is the additive identity for Rational, natural, whole numbers and integers, since multiplying it to them does not change the result.
    <ul>
    <li>3 x 1 = 3</li>
    <li>-4/5 x 1 = -4/5</li>
    </ul>
    </li>
    </ul>
    <p>Hence, 1x a = ax1 = a, where a can be rational number or natural number or whole number of integer.</p>
