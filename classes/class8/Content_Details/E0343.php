            <h3>Special Quadrilaterals requiring less than 5 measurements</h3>
            <p><strong><u>Special Quadrilaterals requiring less than 5 measurements</u></strong></p>
            <p>Earlier quadrilaterals required 5 measurements. Following special quadrilaterals require less than 5 measurements.</p>
            <ul>
            <li><strong>Square</strong> - 1 measurement required. When a side of the square is given, it is true that:
            <ul>
            <li>All sides of the square are equal.</li>
            <li>All internal angles are right angles.</li>
            </ul>
            </li>
            </ul>
            <p>Hence, using SAS condition, the square can be drawn as:</p>
            <p><img src="img/804spequad1.jpg" alt="" width="206" height="187" /></p>
            <p>&nbsp;</p>
            <p><strong>Problem:</strong>Draw a square READ with RE = 5.1 cm.</p>
            <p>&nbsp;</p>
            <p><strong>Solution:</strong></p>
            <p>All sides of a square are equal and the interior angles are 90&deg;.</p>
            <p>Step1: Draw a line RE of 5.1 cm and an angle of 90&deg; at point R and E.</p>
            <p><img src="img/804spequad2.jpg" alt="" width="212" height="170" /></p>
            <p>Step2: As vertex A and D are 5.1 cm away from vertex E and R respectively, cut line segments EA and RD, each of 5.1 cm from these rays.</p>
            <p><img src="img/804spequad3.jpg" alt="" width="216" height="170" /></p>
            <p>Step3: Join D to A.</p>
            <p><img src="img/804spequad4.jpg" alt="" width="199" height="170" /></p>
            <p>&nbsp;</p>
            <p><strong>Rhombus</strong> - 2 measurements required. When two diagonals side of the square is given, it is true that:</p>
            <ul>
            <li>Diagonals of a rhombus are perpendicular bisectors of each other.</li>
            </ul>
            <p>So if a diagonal of given length is drawn and another line is drawn as perpendicular bisector, of again given length, the rhombus can be formed.</p>
            <p>&nbsp;<img src="img/804spequad5.jpg" alt="" width="220" height="179" /></p>
            <p>&nbsp;</p>
            <p><strong>Problem:</strong> Construct a RhombusBEND with following measurements.</p>
            <p>BN = 5.6 cm, DE = 6.5 cm</p>
            <p>&nbsp;</p>
            <p><strong>Solution:</strong></p>
            <p>Step1: Draw Side BN = 5.6 cm and draw its perpendicular bisector. Let it intersect line segment BN at point O</p>
            <p><img src="img/804spequad6.jpg" alt="" width="209" height="247" /></p>
            <p>Step2: Taking O as center, draw arcs of 3.25 cm radius to intersect the perpendicular bisector at point D and E</p>
