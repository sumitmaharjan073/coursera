            <h3>Solving Equations having variables on both sides</h3>
            <p><strong><u>Solving Equations having variables on both sides</u></strong></p>
            <p>The equations having variables in both sides are solved similar to the above.</p>
            <p><em><strong>Example</strong></em>, an equation is 2x - 3 = x + 2</p>
            <p>Adding 3 both sides, 2x - 3 + 3 = x + 2 + 3 or 2x = x + 5</p>
            <p>Subtracting x from both sides, 2x - x = x + 5 - x or <strong>x = 5</strong></p>
