            <h3>Polygons</h3>
            <p><strong><u>Polygons</u></strong></p>
            <p>A simple closed curve made only of line segments is called a <strong>Polygon</strong>.</p>
            <p><img src="img/803poly1.jpg" alt="" width="359" height="354" /></p>
            <p>Polygons are classified based on various factors as indicated below:</p>
            <ul>
            <li><strong>Based on the number of their sides or vertices</strong>.</li>
            </ul>
            <p><img src="img/803poly2.jpg" alt="" width="488" height="281" /></p>
            <ul>
            <li><strong>Based on the portion of the diagonals in exteriors</strong>
            <ul>
            <li><strong>Convex Polygons</strong> - Those which have no portion of their diagonals in their exteriors.</li>
            <li><strong>Concave Polygons</strong> - Those which have at least one portion of their diagonals in their exteriors.</li>
            </ul>
            </li>
            </ul>
            <p><img src="img/803poly3.jpg" alt="" width="337" height="448" /></p>
            <ul>
            <li><strong>Based on size of and angle between the vertices </strong>
            <ol>
            <li><strong>Regular Polygons: </strong>A regular polygon is <strong>equiangular</strong> (angle between any two vertices is equal) and <strong>equilateral</strong> (length of each vertices is equal).</li>
            <li><strong>Irregular Polygons: </strong>An irregular polygon may/may not be equiangular (like a rectangle) but not equilateral.</li>
            </ol>
            </li>
            </ul>
