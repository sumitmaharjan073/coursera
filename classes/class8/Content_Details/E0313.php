    <p><strong><u>Properties of the types of numbers - Closure</u></strong></p>
    <p>A set of numbers is said to be <strong>closed</strong> for a specific mathematical operation if the result obtained when an operation is performed on any two numbers in the set, is itself a member of the set. If a set of numbers is closed for a particular operation then it is said to possess the <strong>closure</strong> property for that operation.</p>
    <ol>
        <li><strong>Whole Numbers</strong></li>
    </ol>
    <ul>
        <li><strong>Addition - </strong>Adding two whole numbers results in another whole number. Hence, whole numbers under addition are <strong>closed</strong>.
        <ul>
        <li>2+3 = 5</li>
        <li>0 + 6 = 6</li>
        </ul>
        </li>
        <li><strong>Subtraction</strong> - Subtracting two whole numbers may result in a negative number which is not a whole number.Hence, whole numbers under subtraction are<strong> notclosed</strong>.
        <ul>
        <li>5-3 = 2 (whole)</li>
        <li>0 - 6 = -6 (not whole)</li>
        </ul>
        </li>
        <li><strong>Multiplication - </strong>Multiplying two whole numbers results in another whole number. Hence, whole numbers under multiplication are <strong>closed</strong>.
        <ul>
        <li>5 x 3 = 15</li>
        <li>2 x 0 = 0</li>
        </ul>
        </li>
        <li><strong>Division</strong> - Dividing two whole numbers may result in a fraction or a number with decimal point which is not a whole number.Hence, whole numbers under subtraction are<strong> notclosed</strong>.
        <ul>
        <li>4 &divide; 2 = 2 (whole)</li>
        <li>10 &divide; 4 = 10/4 = 2.5 (not whole)</li>
        </ul>
        </li>
    </ul>
    <ol start="2">
        <li><strong>Integers</strong></li>
    </ol>
    <ul>
    <li><strong>Addition - </strong>Adding two integers results in another integer. Hence, integers under addition are <strong>closed</strong>.
    <ul>
    <li>(-2) +3 = 1</li>
    <li>(-7) + (-5) = -12</li>
    </ul>
    </li>
    <li><strong>Subtraction</strong> - Subtracting two integers results in another integer.Hence, integersunder subtraction are <strong>closed</strong>.
    <ul>
    <li>5-(-3) = 8</li>
    <li>(-3) - 6 = -9</li>
    </ul>
    </li>
    <li><strong>Multiplication - </strong>Multiplyingtwo integers results in another integer. Hence, integersunder multiplication are <strong>closed</strong>.
    <ul>
    <li>5 x (-3) = -15</li>
    <li>(-2) x (-5) = 10</li>
    </ul>
    </li>
    <li><strong>Division</strong> - Dividing two integersmay result in a fraction or a number with decimal point which is not an integer.Hence, integers under division are<strong> notclosed</strong>.
    <ul>
    <li>4 &divide; (-2) = -2 (Integer)</li>
    <li>(-10) &divide; 4 = -10/4 = -2.5 (not an integer)</li>
    </ul>
    </li>
    </ul>
    <ol start="3">
    <li><strong>Rational Numbers</strong>
    <ul>
    <li><strong>Addition - </strong>Adding two rational numbers results in another rational number. Hence, rational numbersunder addition are <strong>closed</strong>.</li>
    <li>8/5 + (-2)/5 = 6/5</li>
    <li>3/8 + (-5)/7 = (21 + (-40))/56 = -19/56</li>
    <li><strong>Subtraction - </strong>Subtracting two rational numbers results in another rational number. Hence, rational numbersunder subtraction are <strong>closed</strong>.</li>
    <li>8/5 - (-2)/5 = 10/5 = 2 or 2/1</li>
    <li>3/8 -5/7 = (21 - 40)/56 = -19/56</li>
    <li><strong>Multiplication - </strong>Multiplying two rational numbers results in another rational number. Hence, rational numbersunder multiplication are <strong>closed</strong>.</li>
    <li>8/5 x (-2)/5 = -16/25</li>
    <li>(-3)/8 x(-5)/7 = 15/56</li>
    </ul>
    </li>
    </ol>
    <ul>
    <li><strong>Division</strong> - Dividing two rational numbersmay result in an undefined number with which is not a rational number.Hence, rational numbersunder division are<strong> notclosed</strong>.
    <ul>
    <li>4/3 &divide; (-2)/7 = -28/6 = -14/3 (rational number)</li>
    <li>(-10)/3 &divide; 0/1 = -10/0 = undefined (not a rational number)</li>
    </ul>
    </li>
    </ul>
