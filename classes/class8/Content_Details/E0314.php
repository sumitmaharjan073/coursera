    <p><strong><u>Properties of the types of numbers - Associativity</u></strong></p>
    <p>A set of numbers is said to be <strong>associative</strong> for a specific mathematical operation if the result obtained when changing grouping (parenthesizing) of the operands does not change the result.</p>
    <ol>
    <li><strong>Whole Numbers</strong></li>
    </ol>
    <ul>
    <li><strong>Addition &ndash; </strong>Changing the grouping of operands in addition of whole numbers does not change the result. Hence, whole numbers under addition are <strong>associative</strong>.
    <ul>
    <li>2 + (3 + 6) = (2 + 3) + 6</li>
    <li>(0 + 6)+ 8 = 0 + (6 + 8)</li>
    </ul>
    </li>
    <li><strong>Subtraction</strong> - Changing the groupingof operands in subtractionof whole numbers changes the result. Hence, whole numbers under subtraction are <strong>not associative</strong>.
    <ul>
    <li>5 - (3 - 4) &ne; (5 - 3) - 4</li>
    <li>(2 - 0) - 6 &ne; 2 - (0 - 6)</li>
    </ul>
    </li>
    <li><strong>Multiplication - </strong>Changing the groupingof operands in multiplication of whole numbers does not change the result. Hence, whole numbers under multiplication are <strong>associative</strong>.
    <ul>
    <li>5 x (3 x 6) = (5 x 3) x 6</li>
    <li>(2 x 0)x 9 = 2 x (0 x 9)</li>
    </ul>
    </li>
    <li><strong>Division</strong> - Changing the groupingof operands in division of whole numbers changes the result. Hence, whole numbers under division are <strong>not associative.</strong>
    <ul>
    <li>4 &divide; (2 &divide; 6) &ne;(4 &divide; 2) &divide; 6</li>
    <li>(10 &divide; 4)&divide; 7 &ne;10 &divide; (4 &divide; 7)</li>
    </ul>
    </li>
    </ul>
    <ol start="2">
    <li><strong>Integers</strong></li>
    </ol>
    <ul>
    <li><strong>Addition &ndash; </strong>Changing the grouping of operands in addition of integers does not change the result. Hence, integers under addition are <strong>associative</strong>.
    <ul>
    <li>2 + (3 + (-6)) = (2 + 3) + (-6)</li>
    <li>(0 + (-6)) + (-8) = 0 + ((-6) + (-8))</li>
    </ul>
    </li>
    <li><strong>Subtraction</strong> - Changing the grouping of operands in subtractionof integers changes the result. Hence, integers under subtraction are <strong>not associative</strong>.
    <ul>
    <li>5 - (3 - (-4)) &ne; (5 - 3) - (-4)</li>
    <li>((-2) - 0) - 6 &ne; (-2) - (0 - 6)</li>
    </ul>
    </li>
    <li><strong>Multiplication - </strong>Changing the grouping of operands in multiplication of integers does not change the result. Hence, integers under multiplication are <strong>associative</strong>.
    <ul>
    <li>5 x ((-3) x 6) = (5 x (-3)) x 6</li>
    <li>((-2) x 0)x (-9)= (-2) x (0 x (-9))</li>
    </ul>
    </li>
    <li><strong>Division</strong> - Changing the grouping of operands in division of integers changes the result. Hence, integers under division are <strong>not associative.</strong>
    <ul>
    <li>4 &divide; (2 &divide;(-6)) &ne;(4 &divide; 2) &divide;(-6)</li>
    <li>((-10) &divide; 4) &divide; 7 &ne;(-10) &divide; (4 &divide; 7)</li>
    </ul>
    </li>
    </ul>
    <ol start="3">
    <li><strong>Rational Numbers</strong>
    <ul>
    <li><strong>Addition - </strong>Changing the grouping of operands in addition of rational numbers does not change the result. Hence, rational numbers under addition are <strong>associative</strong>.</li>
    <li>2/3 + (3/2 + (-6)/7) = (2/3 + 3/2) + (-6)/7</li>
    <li>(0/1 + (-6)/5) + (-8)/3 = 0/1 + ((-6)/5 + (-8)/3)</li>
    <li><strong>Subtraction - </strong>Changing the grouping of operands in subtractionof rational numbers changes the result. Hence, rational numbers under subtraction are <strong>not associative</strong>.</li>
    <li>5/4 - (3/11 - (-4)/7) &ne; (5/4 - 3/11) - (-4)/7</li>
    <li>((-2)/7 - 0/3) - 6/5 &ne; (-2)/7 - (0/3 - 6/5)</li>
    <li><strong>Multiplication - </strong>Changing the grouping of operands in multiplication of rational numbers does not change the result. Hence, rational numbers under multiplication are <strong>associative</strong>.</li>
    <li>5/3 x ((-3)/4 x 6/11) = (5/3 x (-3)/4) x 6/11</li>
    <li>((-2)/5 x 0/1)x (-9)/2 = (-2)/5 x (0/1 x (-9)/2)</li>
    </ul>
    </li>
    </ol>
    <ul>
    <li><strong>Division</strong> - Changing the grouping of operands in division of rational numbers changes the result. Hence, rational numbers under division are <strong>not associative</strong>.
    <ul>
    <li>4/3 &divide; (2/3 &divide; (-6)/7) &ne;(4/3 &divide; 2/3) &divide; (-6)/7</li>
    <li>((-10)/3 &divide; 4/7) &divide; 7/6 &ne;(-10)/3 &divide; (4/7 &divide; 7/6)</li>
    </ul>
    </li>
    </ul>
